public class ArithmeticOperation {
    public static void main(String[] args) {
        int a, b;

        a = 8;
        b = -7;
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);

    }
}
