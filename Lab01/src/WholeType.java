public class WholeType {
    public static void main(String[] args) {
        byte b1 = 50, b2 = -99, b3 = 87;
        short det = 0, ind = 1;
        int i = -100, j = 100, k = 9999;
        long big = 50, veryBig = 2147483648L;
        char c1 = 'A', c2 = '?', newLine = '\n';

        System.out.println(b1 + " " + b2 + " " + b3 + " " + det + " " + ind + " " + i + " " + j + " " + k + " " + big + " " + veryBig + " " + c1 + " " + c2 + " " + newLine);
    }
}
