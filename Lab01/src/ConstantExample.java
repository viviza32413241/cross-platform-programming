public class ConstantExample {
    public static void main(String[] args) {
        int intConstant = 87;
        System.out.println("Ціла константа: " + intConstant);

        double doubleConstant = 87.83;
        System.out.println("Дійсна константа: " + doubleConstant);

        char charConstant = 'H';
        System.out.println("Чисельні константи: " + charConstant + " " + '\346');

        String stringConstant = "Hello, world!";
        System.out.println("Рядкова константа: " + stringConstant);

    }
}
